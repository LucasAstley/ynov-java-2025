package fr.ynov.java.easy;

public class Arrays {

    public static void main(String[] args) {
        int[] array = new int[10];

        for (int i = 0; i < array.length; i++) {
            array[i] = i + 1;
        }

        for (int i = 0; i < array.length; i++) {
            if (i == array.length - 1) {
                System.out.println(array[i]);
            } else {
            System.out.print(array[i] + ", ");
            }
        }
    }
}
